void	ft_rev_int_tab(int *tab, int size)
{
	int	i;
	int	g;

	size -= 1;
	g = 0;
	while (size > g)
	{
		i = tab[g];
		tab[g] = tab[size];
		tab[size] = i;
		size --;
		g ++;
	}
}
